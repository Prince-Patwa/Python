# create simple calculator

firstNO = int(input("Enter the first no : "))    # input the valu of min and change into int
secondNo = int(input("Enter the second no : "))

print("Sum of given no :",firstNO + secondNo)
print("Sub of given no :",firstNO - secondNo)
print("Mul of given no :",firstNO * secondNo)
print("Div of given no (In int) :",firstNO // secondNo)
print("Div of given no (In float) :",firstNO / secondNo)
print("Reminder of given no :",firstNO % secondNo)